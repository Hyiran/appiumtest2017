package com.oribo.common;

import org.openqa.selenium.WebElement;

import com.oribo.common.Driver;

import io.appium.java_client.android.AndroidElement;

public class FindEelement {
	
	/**
	 *ByID 查找元素 
	 */
	public static  void findByIdClick(String id) {	
		WebElement  personIcon =  Driver.getAndroidDriver(null).findElementById(id);
		personIcon.click();							
	} 

}
